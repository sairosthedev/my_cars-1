// Function to validate custom make/model
export const validateCustomEntry = (value) => {
  // Basic validation for custom entries
  return value.length >= 2 && /^[a-zA-Z0-9\s-]+$/.test(value);
};

export const generateVIN = (make, model, year) => {
  const makeCode = make.substring(0, 3).toUpperCase();
  const modelCode = model.substring(0, 2).toUpperCase();
  const yearCode = year.toString().substring(2);
  const randomChars = Math.random().toString(36).substring(2, 11).toUpperCase();
  return `${makeCode}${modelCode}${yearCode}${randomChars}`;
}; 